def get_genre_name(code: str) -> str:
    """ジャンルコードを日本語名に変換"""
    return GENRE_CODES.get(code, code)


def get_budget_name(code: str) -> str:
    """予算コードを日本語名に変換"""
    return BUDGET_CODES.get(code, code)


def get_genre_names(codes: list) -> list:
    """複数のジャンルコードを日本語名リストに変換"""
    return [get_genre_name(code) for code in codes]
